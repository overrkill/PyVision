from graphics import *
print("DDA Line")
x=int(input("coords of x1"))
y=int(input("coords of y1"))
x2=int(input("coords of x2"))
y2=int(input("coords of y2"))
dy=y2-y
dx=x2-x
step=0.0
if abs(dx)>abs(dy):
    step=dx
else:
    step=dy
print(step)
xin=float(dx)/step
yin=float(dy)/step
print("inc x y ",xin,yin)
win=GraphWin("DDA line",500,500)
Point(x,y).draw(win)
for i in range(step):
    x=x+xin
    y=y+yin
    print(x,y)
    Point(round(x),round(y)).draw(win)
win.getMouse()	
win.close()
